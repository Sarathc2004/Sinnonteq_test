class FoodItem {
  final String name;
  final double price;
  final double rating;
  final int ratingCount;
  final String description;
  final String imageUrl;

  FoodItem({
    required this.name,
    required this.price,
    required this.rating,
    required this.ratingCount,
    required this.description,
    required this.imageUrl,
  });
}
